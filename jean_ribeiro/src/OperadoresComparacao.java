public class OperadoresComparacao {
    public static void main(String[] args) {

        // Operadores de Comparação
        // Utilização:  a <operador> b
        // == Igual
        // != Diferente
        // > Maior que
        // >= Maior ou igual
        // < Menor que
        // <= Menor ou igual

        int a = 10;
        int b = 5;

        System.out.println(a == b); //false
        System.out.println(a != b); //true
        System.out.println(a > b);  //true
        System.out.println(a >= b); //true
        System.out.println(a < b);  //false
        System.out.println(a <= b); //false
    }
}